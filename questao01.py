INDICE = 13
SOMA = 0
K = 0

while K < INDICE:
    K = K + 1
    SOMA = SOMA + K

print(SOMA)


# A variável SOMA armazenará o resultado da soma dos números de 1 até 13,
# que é 91. Isso ocorre porque o loop while incrementa a variável K de 1 em 1,
# adicionando cada valor de K à variável SOMA, até que K seja igual a 13.
# Portanto, ao final do processamento, SOMA será igual a 91.
